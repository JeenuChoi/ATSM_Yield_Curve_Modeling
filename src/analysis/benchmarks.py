import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import os
from sklearn.linear_model import LinearRegression
from scipy.optimize import minimize
from src.analysis.forecasting import diebold_mariano_test

class DieboldLiModel:
    """
    Implements Dynamic Nelson-Siegel (DNS) Model (Diebold and Li, 2006).
    DNS Factor Loadings: [1, (1-exp(-lt))/lt, (1-exp(-lt))/lt - exp(-lt)]
    Factors are then forecasted using recursive AR(1).
    """
    def __init__(self, lmbda=0.0609): # Standard lambda from Diebold & Li
        self.lmbda = lmbda
        self.factors = None
        self.betas = None
        
    def get_loadings(self, maturities):
        lt = self.lmbda * maturities
        l1 = np.ones_like(maturities)
        l2 = (1 - np.exp(-lt)) / lt
        l3 = l2 - np.exp(-lt)
        return np.vstack([l1, l2, l3]).T

    def fit_factors(self, yields, maturities):
        """
        Step 1: Cross-sectional OLS to find Beta_t (Level, Slope, Curvature) for each t.
        """
        loadings = self.get_loadings(maturities)
        betas = []
        for t in range(len(yields)):
            y_t = yields[t, :]
            model = LinearRegression().fit(loadings, y_t)
            betas.append(model.coef_)
        self.factors = pd.DataFrame(betas, columns=['L', 'S', 'C'])
        return self.factors

    def forecast_1step(self, yields, maturities, oos_start_date, dates):
        """
        Step 2: Recursive AR(1) on factors to forecast y_{t+1}.
        """
        loadings = self.get_loadings(maturities)
        self.fit_factors(yields, maturities)
        self.factors.index = dates
        
        oos_dates = dates[dates >= oos_start_date]
        forecasts = []
        
        for date in oos_dates:
            # Training factors up to t
            train_factors = self.factors.loc[:date - pd.Timedelta(days=1)]
            
            # 1-step forecast for each factor using AR(1)
            pred_betas = []
            for col in ['L', 'S', 'C']:
                y = train_factors[col].values[1:]
                X = train_factors[col].values[:-1].reshape(-1, 1)
                model = LinearRegression().fit(X, y)
                pred_betas.append(model.predict([[train_factors[col].iloc[-1]]])[0])
            
            # Combine forecasted factors with loadings
            y_pred = loadings @ np.array(pred_betas)
            forecasts.append(y_pred)
            
        return pd.DataFrame(forecasts, index=oos_dates, columns=maturities)

def run_ar1_benchmark(yield_series, oos_start_date):
    """
    Recursive AR(1) on the yield itself.
    """
    oos_dates = yield_series.index[yield_series.index >= oos_start_date]
    preds = []
    for date in oos_dates:
        train = yield_series.loc[:date - pd.Timedelta(days=1)]
        X = train.values[:-1].reshape(-1, 1)
        y = train.values[1:]
        model = LinearRegression().fit(X, y)
        preds.append(model.predict([[train.iloc[-1]]])[0])
    return pd.Series(preds, index=oos_dates)

def get_rmse(actual, pred):
    return np.sqrt(np.mean((actual - pred)**2)) * 10000

def run_final_benchmarking():
    print("\n" + "="*60)
    print("--- [FINAL BENCHMARKING] ATSM vs RW vs AR(1) vs DIEBOLD-LI ---")
    print("="*60)
    
    # 1. Load Data
    try:
        df_actual = pd.read_csv("outputs/observed_yields.csv", index_col=0, parse_dates=True)
        # Load the TRUE OOS forecasts generated by main6.py
        df_atsm = pd.read_csv("outputs/true_oos_forecasts.csv", index_col=0, parse_dates=True) 
    except:
        print("Data files not found in outputs/. Please run main6.py first.")
        return

    MATURITIES = np.array([1, 2, 3, 5, 7, 10, 20, 30], dtype=float)
    OOS_START = "2019-01-31"
    
    # Target: 10Y Yield
    y_actual = df_actual['10.0'].dropna()
    y_atsm = df_atsm['10.0'].loc[OOS_START:]
    
    # 2. Random Walk
    y_rw = y_actual.shift(1).loc[OOS_START:]
    
    # 3. AR(1)
    y_ar1 = run_ar1_benchmark(y_actual, OOS_START)
    
    # 4. Diebold-Li (DNS)
    dl_model = DieboldLiModel()
    df_dl = dl_model.forecast_1step(df_actual.values, MATURITIES, OOS_START, df_actual.index)
    y_dl = df_dl[10.0]
    
    # Align all
    common_idx = y_actual.loc[OOS_START:].index.intersection(y_atsm.index).intersection(y_dl.index)
    y_actual = y_actual.loc[common_idx]
    y_atsm = y_atsm.loc[common_idx]
    y_rw = y_rw.loc[common_idx]
    y_ar1 = y_ar1.loc[common_idx]
    y_dl = y_dl.loc[common_idx]
    
    # 5. Calculate Metrics
    rmse_atsm = get_rmse(y_actual, y_atsm)
    rmse_rw = get_rmse(y_actual, y_rw)
    rmse_ar1 = get_rmse(y_actual, y_ar1)
    rmse_dl = get_rmse(y_actual, y_dl)
    
    # 6. DM Tests (ATSM vs Everyone)
    _, p_rw = diebold_mariano_test(y_actual, y_atsm, y_rw)
    _, p_ar1 = diebold_mariano_test(y_actual, y_atsm, y_ar1)
    _, p_dl = diebold_mariano_test(y_actual, y_atsm, y_dl)
    
    print(f"\nRMSE Results (10Y Yield, OOS 2019-2026):")
    print(f" - [OURS] ATSM-Macro:  {rmse_atsm:.2f} bps")
    print(f" - [BENCH] Diebold-Li: {rmse_dl:.2f} bps")
    print(f" - [BENCH] AR(1):      {rmse_ar1:.2f} bps")
    print(f" - [BENCH] Random Walk: {rmse_rw:.2f} bps")
    
    print(f"\nDiebold-Mariano P-values (ATSM vs ...):")
    print(f" - vs. Diebold-Li:  p = {p_dl:.4f}")
    print(f" - vs. AR(1):       p = {p_ar1:.4f}")
    print(f" - vs. Random Walk: p = {p_rw:.4f}")
    
    # 7. Visualization
    plt.figure(figsize=(12, 6))
    plt.plot(y_actual.index, y_actual*100, 'k-', label='Actual 10Y', linewidth=2)
    plt.plot(y_atsm.index, y_atsm*100, 'b--', label='ATSM-Macro (Ours)')
    plt.plot(y_dl.index, y_dl*100, 'g:', label='Diebold-Li (2006)')
    plt.plot(y_rw.index, y_rw*100, 'r-.', label='Random Walk', alpha=0.5)
    plt.title("OOS Forecast Comparison: ATSM vs Benchmarks")
    plt.ylabel("Yield (%)")
    plt.legend()
    plt.grid(True, alpha=0.3)
    plt.savefig("outputs/oos_benchmark_comparison.png")
    
    print("\nVisual saved to: outputs/oos_benchmark_comparison.png")

if __name__ == "__main__":
    run_final_benchmarking()
